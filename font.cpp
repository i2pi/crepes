unsigned char font [38][14][16];
unsigned char font_width[38];

inline
void	srpixel (int x, int y, unsigned long c, unsigned char *screen)
{
	screen[x+y*SWIDTH] = (unsigned char) ((c>>4)<<4)&0xff;
}

void	prints (char *s, int X, int Y, unsigned char *screen)
{
	int	x,y,n,l;
	int	j,k;
	unsigned long c, b;

	x = X;
	y = Y;
	l=0;
	b=0;

	while (s[l] != '\0')
	{
		if ((s[l] >= 'a')&&(s[l] <= 'z')) n = s[l]-'a'; else
		if ((s[l] >= '0')&&(s[l] <= '9')) n = s[l]-'0' + 26; else
		if (s[l] == ',') n = 36; else 
		if (s[l] == '.') n = 37; else n = -65;
		
		if (s[l] == '\n')
		{
			x = X-6;
			y += 15;
		}

		if (n != -65)
		{
			for (j=0; j<font_width[n]; j++)
			for (k=0; k<16; k++)
			{
				if (font[n][j][k] != 255)
				{
					c = rgb (font[n][j][k], font[n][j][k], font[n][j][k]);
					if ((x+j<WIDTH)&&(y+k<HEIGHT))
					{
							srpixel (x+j, y+k, c, screen);
					}
				}
			}	
			if (b)
			{
				for (j=0; j<font_width[n]-2; j++)
				{
					srpixel (x+j, y+16, 0x0000ff, screen);
				}
			}
			x+=font_width[n]-2;
		} else
		{
			x+=6;
		}
		
		l++;
	}
}
inline
void	rpixel (int x, int y, unsigned long c)
{
	tcono[x+y*WIDTH] = c;
}

